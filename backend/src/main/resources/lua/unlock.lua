-- ============================================================
-- city-review 分布式锁释放脚本
-- 原子性：判断锁归属 → 释放锁，防止误删
-- ============================================================

-- KEYS[1]: 锁的 key
-- ARGV[1]: 当前线程标示（UUID + threadId）

-- 判断锁是否属于当前线程
if (redis.call('GET', KEYS[1]) == ARGV[1]) then
    -- 属于，释放锁
    return redis.call('DEL', KEYS[1])
end
-- 不属于，返回 0
return 0
